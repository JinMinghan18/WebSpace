package com.model;

public class IP {
    private String IP;
    private String visit_time;
    public IP(){}

    public void setVisit_time(String visit_time) {
        this.visit_time = visit_time;
    }

    public void setIP(String IP) {
        this.IP = IP;
    }

    public String getVisit_time() {
        return visit_time;
    }

    public String getIP() {
        return IP;
    }
}
