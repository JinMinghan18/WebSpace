package com.model;

public class newpaper {
    private String paper_name;
    private String paper_time;
    private String paper_category;
    private String paper_introduce;

    public newpaper() {
    }

    public String getPaper_name() {
        return paper_name;
    }

    public void setPaper_name(String paper_name) {
        this.paper_name = paper_name;
    }

    public String getPaper_time() {
        return paper_time;
    }

    public void setPaper_time(String paper_time) {
        this.paper_time = paper_time;
    }

    public String getPaper_category() {
        return paper_category;
    }

    public void setPaper_category(String paper_category) {
        this.paper_category = paper_category;
    }

    public String getPaper_introduce() {
        return paper_introduce;
    }

    public void setPaper_introduce(String paper_introduce) {
        this.paper_introduce = paper_introduce;
    }
}
