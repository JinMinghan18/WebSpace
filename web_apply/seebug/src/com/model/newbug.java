package com.model;

public class newbug {
    private String SSV_ID;
    private String update_time;
    private String bug_level;
    private String bug_name;
    private String bug_status;

    public newbug() {
    }

    public String getSSV_ID() {
        return SSV_ID;
    }

    public void setSSV_ID(String SSV_ID) {
        this.SSV_ID = SSV_ID;
    }

    public String getUpdate_time() {
        return update_time;
    }

    public void setUpdate_time(String update_time) {
        this.update_time = update_time;
    }

    public String getBug_level() {
        return bug_level;
    }

    public void setBug_level(String bug_level) {
        this.bug_level = bug_level;
    }

    public String getBug_name() {
        return bug_name;
    }

    public void setBug_name(String bug_name) {
        this.bug_name = bug_name;
    }

    public String getBug_status() {
        return bug_status;
    }

    public void setBug_status(String bug_status) {
        this.bug_status = bug_status;
    }
}

